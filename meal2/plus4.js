var count_cheese = 1;
var count_cheeseEl = document.getElementById('count_cheese');
function plus_cheese() {
    count_cheese++;
    count_cheeseEl.value = count_cheese;
}
function minus_cheese() {
    if (count_cheese > 1) {
        count_cheese--;
        count_cheeseEl.value = count_cheese;
    }
}
